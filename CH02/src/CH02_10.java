/* 移位運算子 2進位 
 * <<: 左移
 * >>: 右移
 * [整數值] << [移位值]
 */

public class CH02_10
{

	public static void main(String[] args)
	{
		System.out.println("5 << 2 return value: " + (5 << 2));   
		System.out.println("-5 << 2 return value: " + (-5 << 2)); 
		System.out.println("5 >> 2 return value: " + (5 >> 2));	  
		System.out.println("-5 >> 2 return value:" + (-5 >> 2));  
	}

}
